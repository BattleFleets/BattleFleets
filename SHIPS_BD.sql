CREATE OR REPLACE  FUNCTION CREATE_CANNON(objectIdTemplate NUMBER) RETURN NUMBER is
	currvalue NUMBER;
    cannonNameId NUMBER := 14;
    objectTypeId NUMBER := 8;
    templateTypeId NUMBER := 3;
    name_ob OBJECTS.NAME%TYPE;
